class Arithmetic
{
	public static void main(String arr[])
	{
		int a=10;
		int b=2;
		int add=a+b;
		int sub=a-b;

		int c=10;
		double d=12.5;
		double mul=c*d;
		double div=c/d;
		double mod=c%d;

		System.out.println("value of add is "+add);
		System.out.println("value of sub is "+sub);
		System.out.println("value of mul is "+mul);
		System.out.println("value of div is "+div);
		System.out.println("value of mod is "+mod);
}
}
