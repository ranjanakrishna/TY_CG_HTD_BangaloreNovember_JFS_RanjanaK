class Switch
{
	public static void main(String ar[])
	{
		int a=20;
		switch(a){
		case 10:System.out.println("value of a is 10");
			break;
		case 20:System.out.println("value of a is 20");
			break;
		}
		
		String name="abc";
		switch(name){
		case "abc":System.out.println("name is abc");
			break;
		case "xyz":System.out.println("name is xyz");
			break;
		}

		char c='c';
		switch(c){
		case 'a':System.out.println("char is a");
			break;
		case 'c':System.out.println("char is c");
			break;
		}

		
		
		
	}
}